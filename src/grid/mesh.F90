  ! -------------------------------------------------------------- 
  !> Read the mesh parameters from file that were written by mesh_dump.
  subroutine mesh_init_from_file(mesh, iunit)
    type(mesh_t), intent(inout) :: mesh
    integer,      intent(in)    :: iunit

    character(len=20)  :: str
    character(len=100) :: line

    PUSH_SUB(mesh_init_from_file)

    ! Find (and throw away) the dump tag.
    do
      read(iunit, '(a)') line
      if(trim(line).eq.dump_tag) exit
    end do

    ASSERT(mesh%sb%dim > 0 .and. mesh%sb%dim <= MAX_DIM)

    mesh%idx%nr = 0
    mesh%idx%ll = 0
    mesh%idx%enlarge = 0
    read(iunit, '(a20,7i8)')  str, mesh%idx%nr(1, 1:mesh%sb%dim)
    read(iunit, '(a20,7i8)')  str, mesh%idx%nr(2, 1:mesh%sb%dim)
    read(iunit, '(a20,7i8)')  str, mesh%idx%ll(1:mesh%sb%dim)
    ! ll should not be read but computed:
    mesh%idx%ll(:) = mesh%idx%nr(2, :) - mesh%idx%nr(1, :) + 1
    read(iunit, '(a20,7i8)')  str, mesh%idx%enlarge(1:mesh%sb%dim)
    read(iunit, '(a20,1i10)') str, mesh%np
    read(iunit, '(a20,1i10)') str, mesh%np_part
    read(iunit, '(a20,1i10)') str, mesh%np_global
    read(iunit, '(a20,1i10)') str, mesh%np_part_global
    nullify(mesh%idx%lxyz, mesh%idx%lxyz_inv, mesh%x, mesh%vol_pp, mesh%resolution)
    mesh%parallel_in_domains = .false.

    POP_SUB(mesh_init_from_file)
  end subroutine mesh_init_from_file


  ! --------------------------------------------------------------
  subroutine mesh_write_fingerprint(mesh, filename)
    type(mesh_t),     intent(in) :: mesh
    character(len=*), intent(in) :: filename

    integer :: iunit

    PUSH_SUB(mesh_write_fingerprint)

    if(mesh%sb%box_shape /= HYPERCUBE) then

      iunit = io_open(trim(filename), action='write', is_tmp=.true.)
      write(iunit, '(a20,i21)')  'np_part_global=     ', mesh%np_part_global
      write(iunit, '(a20,i21)')  'np_global=          ', mesh%np_global
      write(iunit, '(a20,i21)')  'algorithm=          ', 1
      write(iunit, '(a20,i21)')  'checksum=           ', mesh%idx%checksum
      call io_close(iunit)

    end if

    POP_SUB(mesh_write_fingerprint)
  end subroutine mesh_write_fingerprint

  ! -----------------------------------------------------------------------
  ! This function reads the fingerprint of a mesh written in
  ! filename. If the mesh are equal (same fingerprint) return values
  ! are 0, otherwise it returns the size of the mesh stored. If the
  ! fingerprint cannot be read, it returns -1.

  subroutine mesh_read_fingerprint(mesh, filename, read_np_part, read_np)
    type(mesh_t),     intent(in)  :: mesh
    character(len=*), intent(in)  :: filename
    integer,          intent(out) :: read_np_part
    integer,          intent(out) :: read_np

    character(len=20)  :: str
    integer :: iunit, algorithm
    integer(8) :: checksum

    PUSH_SUB(mesh_read_fingerprint)

    iunit = io_open(trim(filename), action='read', status='old', die=.false., is_tmp = .true.)

    if(iunit < 0) then
      read_np_part = -1
      read_np = -1
    else

      read(iunit, '(a20,i21)')  str, read_np_part
      read(iunit, '(a20,i21)')  str, read_np
      read(iunit, '(a20,i21)')  str, algorithm
      read(iunit, '(a20,i21)')  str, checksum
      
      call io_close(iunit)

      ASSERT(read_np_part >= read_np)
      
      if(read_np_part == mesh%np_part_global &
        .and. read_np == mesh%np_global &
        .and. algorithm == 1 &
        .and. checksum == mesh%idx%checksum) then
        read_np_part = 0
        read_np = 0
      end if

    endif

    POP_SUB(mesh_read_fingerprint)

  end subroutine mesh_read_fingerprint

  ! --------------------------------------------------------------
  !> Fill the lxyz and lxyz_inv arrays from a file
  subroutine mesh_lxyz_init_from_file(mesh, filename)
    type(mesh_t),     intent(inout) :: mesh
    character(len=*), intent(in) :: filename

    integer :: ip, idir, ix(MAX_DIM), ierr

    PUSH_SUB(mesh_lxyz_init_from_file)

    ASSERT(mesh%sb%dim > 0 .and. mesh%sb%dim <= MAX_DIM)

    ! FIXME 4D
    ASSERT(mesh%sb%box_shape /= HYPERCUBE)

    call io_binary_read(trim(filename)//'.obf', mesh%np_part*mesh%sb%dim, mesh%idx%lxyz, ierr)

    if(ierr > 0) then
      message(1) = "Error: failed to read file "//trim(filename)//'.obf'
      call messages_fatal(1)
    end if

    do ip = 1, mesh%np_part
      forall (idir = 1:mesh%sb%dim) ix(idir) = mesh%idx%lxyz(ip, idir)
      forall (idir = mesh%sb%dim + 1:MAX_DIM) ix(idir) = 0
      mesh%idx%lxyz_inv(ix(1), ix(2), ix(3)) = ip
    end do

    POP_SUB(mesh_lxyz_init_from_file)
  end subroutine mesh_lxyz_init_from_file


  ! --------------------------------------------------------------
  !> Extracts the point numbers of a rectangular subset spanned
  !! by the two corner points from and to.
  subroutine mesh_subset_indices(mesh, from, to, indices)
    type(mesh_t), intent(in)  :: mesh
    integer,      intent(in)  :: from(MAX_DIM)
    integer,      intent(in)  :: to(MAX_DIM)
    integer,      intent(out) :: indices(:)

    integer :: lb(MAX_DIM) !< Lower bound of indices.
    integer :: ub(MAX_DIM) !< Upper bound of indices.

    integer :: ix, iy, iz, ii

    PUSH_SUB(mesh_subset_indices)

    ! In debug mode, check for valid indices in from, to first.
    if(in_debug_mode) then
      if(.not.index_valid(mesh, from).or..not.index_valid(mesh, to)) then
        message(1) = 'Failed assertion:'
        message(2) = 'mesh.mesh_subset_indices has been passed points outside the box:'
        message(3) = ''
        write(message (4), '(a, i6, a, i6, a, i6, a)') &
          '  from = (', from(1), ', ', from(2), ', ', from(3), ')'
        write(message(5), '(a, i6, a, i6, a, i6, a)') & 
          '  to   = (', to(1), ', ', to(2), ', ', to(3), ')'
        call messages_fatal(5)
      end if
    end if

    lb = min(from, to)
    ub = max(from, to)

    ii = 1
    do ix = lb(1), ub(1)
      do iy = lb(2), ub(2)
        do iz = lb(3), ub(3)
          indices(ii) = mesh%idx%lxyz_inv(ix, iy, iz)
          ii         = ii + 1
        end do
      end do
    end do

    POP_SUB(mesh_subset_indices)
  end subroutine mesh_subset_indices


  ! --------------------------------------------------------------
  !> Checks if the (x, y, z) indices of point are valid, i.e.
  !! inside the dimensions of the simulation box.
  logical function index_valid(mesh, point)
    type(mesh_t), intent(in) :: mesh
    integer,      intent(in) :: point(MAX_DIM)

    integer :: idir
    logical :: valid

    PUSH_SUB(index_valid)

    valid = .true.
    do idir = 1, mesh%sb%dim
      if(point(idir) .lt. mesh%idx%nr(1, idir) .or. point(idir) .gt. mesh%idx%nr(2, idir)) then
        valid = .false.
      end if
    end do

    index_valid = valid

    POP_SUB(index_valid)
  end function index_valid


  ! --------------------------------------------------------------
  recursive subroutine mesh_end(mesh, is_lead)
    type(mesh_t), intent(inout)   :: mesh
    logical, intent(in), optional :: is_lead

    logical :: is_lead_
#ifdef HAVE_MPI
    integer :: ipart
#endif

    PUSH_SUB(mesh_end)

    call mesh_cube_map_end(mesh%cube_map)

    if (present(is_lead)) then
      is_lead_ = is_lead
    else
      is_lead_ = .false.
    end if

    if (.not. is_lead_) then
      if(mesh%idx%sb%box_shape == HYPERCUBE) call hypercube_end(mesh%idx%hypercube)
    end if

    SAFE_DEALLOCATE_P(mesh%resolution)
    SAFE_DEALLOCATE_P(mesh%idx%lxyz)
    SAFE_DEALLOCATE_P(mesh%idx%lxyz_inv)
    SAFE_DEALLOCATE_P(mesh%x)
    SAFE_DEALLOCATE_P(mesh%vol_pp)

    if(mesh%parallel_in_domains) then
#if defined(HAVE_MPI)
      call vec_end(mesh%vp)
#endif
    end if

    POP_SUB(mesh_end)
  end subroutine mesh_end

  
  ! This function returns the point inside the grid corresponding to
  ! a boundary point when PBCs are used. In case the point does not
  ! have a correspondence (i.e. other BCs are used in that direction),
  ! the same point is returned. Note that this function returns a
  ! global point number when parallelization in domains is used.
  ! ---------------------------------------------------------  
  integer function mesh_periodic_point(mesh, ip) result(ipp)
    type(mesh_t), intent(in)    :: mesh
    integer,      intent(in)    :: ip
    
    integer :: ix(MAX_DIM), nr(2, MAX_DIM), idim
    
    ! no push_sub, called too frequently

    ix = mesh%idx%lxyz(ip, :)
    nr(1, :) = mesh%idx%nr(1, :) + mesh%idx%enlarge(:)
    nr(2, :) = mesh%idx%nr(2, :) - mesh%idx%enlarge(:)
    
    do idim = 1, mesh%sb%periodic_dim
      if(ix(idim) < nr(1, idim)) ix(idim) = ix(idim) + mesh%idx%ll(idim)
      if(ix(idim) > nr(2, idim)) ix(idim) = ix(idim) - mesh%idx%ll(idim)
    end do
    
    ipp = mesh%idx%lxyz_inv(ix(1), ix(2), ix(3))
    
  end function mesh_periodic_point
  

  ! ---------------------------------------------------------
  real(8) pure function mesh_global_memory(mesh) result(memory)
    type(mesh_t), intent(in) :: mesh
    
    memory = 0.0_8
    
    ! lxyz_inv
    memory = memory + SIZEOF_UNSIGNED_INT * product(mesh%idx%nr(2, 1:mesh%sb%dim) - mesh%idx%nr(1, 1:mesh%sb%dim) + M_ONE)
    ! resolution
    if(mesh%sb%mr_flag) then
      memory = memory + SIZEOF_UNSIGNED_INT * product(mesh%idx%nr(2, 1:mesh%sb%dim) - mesh%idx%nr(1, 1:mesh%sb%dim) + M_ONE)
    end if
    ! lxyz
    memory = memory + SIZEOF_UNSIGNED_INT * dble(mesh%np_part_global) * MAX_DIM

  end function mesh_global_memory


  ! ---------------------------------------------------------
  real(8) pure function mesh_local_memory(mesh) result(memory)
    type(mesh_t), intent(in) :: mesh
    
    memory = 0.0_8
    
    ! x
    memory = memory + REAL_PRECISION * dble(mesh%np_part) * MAX_DIM
  end function mesh_local_memory


  ! ---------------------------------------------------------
  function mesh_x_global(mesh, ip, force) result(xx)
    type(mesh_t),       intent(in) :: mesh
    integer,            intent(in) :: ip
    logical, optional,  intent(in) :: force
    FLOAT                          :: xx(1:MAX_DIM)

    FLOAT :: chi(1:MAX_DIM)
    integer :: ix(1:MAX_DIM)
    logical :: force_

! no push_sub because function is called too frequently

    force_ = .false.
    if (present(force)) force_ = force
      
    if(mesh%parallel_in_domains .or. force_) then
      call index_to_coords(mesh%idx, mesh%sb%dim, ip, ix)
      chi(1:mesh%sb%dim) = ix(1:mesh%sb%dim) * mesh%spacing(1:mesh%sb%dim)
      chi(mesh%sb%dim + 1:MAX_DIM) = M_ZERO
      xx = M_ZERO ! this initialization is required by gfortran 4.4 or we get NaNs
      call curvilinear_chi2x(mesh%sb, mesh%cv, chi, xx)
      xx(mesh%sb%dim + 1:MAX_DIM) = M_ZERO
    else
      xx(1:MAX_DIM) = mesh%x(ip, 1:MAX_DIM)
    end if

  end function mesh_x_global


  ! ---------------------------------------------------------
  logical pure function mesh_compact_boundaries(mesh) result(cb)
    type(mesh_t),       intent(in) :: mesh
    
    cb = .not. mesh%use_curvilinear .and. &
         .not. mesh%parallel_in_domains .and.  &
         simul_box_has_zero_bc(mesh%sb)

  end function mesh_compact_boundaries

